def flatten(universe: dict) -> list[str]:
    tickers = []
    for group in universe.values():
        tickers.extend(group)
    return tickers


def get_signal(regime_id: int, rsi: float) -> str:
    if regime_id == 4:
        return "AVOID"
    if regime_id == 3 and rsi < 35:
        return "WATCH (oversold in trend)"
    if regime_id == 3:
        return "CAUTION"
    if regime_id in (1, 2) and rsi < 30:
        return "OPPORTUNITY"
    if regime_id in (1, 2) and rsi < 40:
        return "BUILDING"
    return "NORMAL"


def scan_market(ma, tickers: list[str], market_label: str) -> list[dict]:
    """Scan tickers and return regime + technical data."""
    results = []
    for t in tickers:
        try:
            r = ma.regime.detect(t)
            tech = ma.technicals.snapshot(t)

            # IV rank (broker only)
            ivr = None
            try:
                m = ma.quotes.get_metrics(t)
                if m and hasattr(m, "iv_rank") and m.iv_rank:
                    ivr = m.iv_rank
                elif isinstance(m, dict) and m.get("ivRank"):
                    ivr = m["ivRank"]
            except Exception:
                pass

            results.append({
                "ticker": t,
                "regime": r.regime.value,
                "confidence": r.confidence,
                "rsi": tech.rsi.value,
                "atr_pct": tech.atr_pct,
                "price": tech.current_price,
                "iv_rank": ivr,
                "signal": get_signal(r.regime.value, tech.rsi.value),
                "bb_pct_b": tech.bollinger.percent_b if tech.bollinger else None,
            })
        except Exception as e:
            results.append({"ticker": t, "error": str(e)[:60]})
    return results


